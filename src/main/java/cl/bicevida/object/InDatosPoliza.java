package cl.bicevida.object;

public class InDatosPoliza {

	
	 protected String descripcionPoliza;
	 protected String numeroContratoPoliza;
	 protected Long numeroPoliza;
	 protected Long numeroPropuesta;
	 protected Long ramoPoliza;
	
	public InDatosPoliza(){
		
	}

	public String getDescripcionPoliza() {
		return descripcionPoliza;
	}

	public void setDescripcionPoliza(String descripcionPoliza) {
		this.descripcionPoliza = descripcionPoliza;
	}

	public String getNumeroContratoPoliza() {
		return numeroContratoPoliza;
	}

	public void setNumeroContratoPoliza(String numeroContratoPoliza) {
		this.numeroContratoPoliza = numeroContratoPoliza;
	}

	public Long getNumeroPoliza() {
		return numeroPoliza;
	}

	public void setNumeroPoliza(Long numeroPoliza) {
		this.numeroPoliza = numeroPoliza;
	}

	public Long getNumeroPropuesta() {
		return numeroPropuesta;
	}

	public void setNumeroPropuesta(Long numeroPropuesta) {
		this.numeroPropuesta = numeroPropuesta;
	}

	public Long getRamoPoliza() {
		return ramoPoliza;
	}

	public void setRamoPoliza(Long ramoPoliza) {
		this.ramoPoliza = ramoPoliza;
	}
	

	
	
}
